from .. import outputs
import io

@outputs.output('sys')
@outputs.output('box')
class JumptableOutput(outputs.Output):
    """
    Name of source file to target for building a jumptable.
    """
    __argname__ = "jumptable"
    __arghelp__ = __doc__

    def __init__(self, path):
        self.path = path
        self._includes = []
        self._decls = []

    def append_include(self, include):
        if not include.startswith('"') and not include.startswith('<'):
            include = '"%s"' % include
        if include not in self._includes:
            self._includes.append(include)

    def append_decl(self, decl=None):
        outf = io.StringIO()
        self._decls.append(outf)
        if decl is not None:
            outf.write(decl)
        return outf

    def build(self, sys, box, outf):
        outf.write('////// AUTOGENERATED //////\n')
        for include in self._includes:
            outf.write('#include %s\n' % include)
        outf.write('\n')
        for decl in self._decls:
            outf.write(decl.getvalue())
            outf.write('\n')
